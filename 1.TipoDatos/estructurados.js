console.log(typeof({})) // "object" -> Un objeto vacío
console.log(typeof([])) //array
// ¿Qué es?
// {}  -> Es un objeto vacío, estructura para almacenar pares clave:valor.
// []  -> Es un array vacío, estructura para almacenar listas ordenadas de elementos.
// Ambos son de tipo "object" según typeof, pero puedes diferenciar un array usando Array.isArray([]) que devuelve true solo para arrays.